DECLARE
    @V_CONT_FORD INT,
    @V_CONT_FIAT INT
BEGIN
    --MÉTODO 1 - SELECT RETORNA COLUNA E UM RESULTADO
    SET @V_CONT_FORD = (SELECT COUNT(*) FROM CARROS
        WHERE FABRICANTE = 'FORD')

    PRINT 'QUANTIDADE FORD: [+] ' + CAST(@V_CONT_FORD AS VARCHAR)

